let myVariable = 5;        // Initially, myVariable is of type number
console.log(typeof myVariable); // Outputs: 'number'

myVariable = "Hello";      // Now, myVariable is reassigned to a string
console.log(typeof myVariable); // Outputs: 'string'

myVariable = true;         // Finally, myVariable is reassigned to a boolean
console.log(typeof myVariable); // Outputs: 'boolean'
